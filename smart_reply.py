
import re

def generate_bot_reply(customer_message):
    # 1. تنظيف النص وتقطيعه (الدرس السابق)
    cleaned_text = re.sub(r'[^\w\s]', '', customer_message)
    words = cleaned_text.split()
    
    # 2. منطق فحص الكلمات المفتاحية والرد الذكي
    if "زيت" in words and "الإكليل" in words:
        return "زيت إكليل الجبل متوفر لدينا! سعره 45 ريال، وهو ممتاز لتنبيت الشعر وتكثيفه. هل ترغبين في إضافته لسلتك؟"
        
    elif "شامبو" in words or "البيوتين" in words:
        return "أهلاً بكِ! شامبو البيوتين متوفر بسعر 35 ريال، ومخصص لعلاج الشعر التالف والمتساقط."
        
    elif "سعر" in words or "بكم" in words:
        return "يمكنكِ الاطلاع على أسعار كافة المنتجات منيو متجر ذوق وجمال من هنا: [رابط المتجر]."

    elif "سيروم" in words or "الأرجان" in words:
        return "سيروم الأرجان المغربي الأصلي متوفر بسعر 60 ريال، يعيد الحيوية للشعر الجاف والمتقصف."
        
    else:
        return "مرحباً بكِ في متجر ذوق وجمال! يرجى كتابة اسم المنتج الذي تبحثين عنه (سيروم، شامبو، زيت) لمساعدتكِ فوراً."

# --- تجربة النظام عملياً ---
# محاكاة لرسالة حقيقية وصلت على الواتساب
incoming_message = "أريد طلب سيروم الأرجان"

# تشغيل الدالة والحصول على الرد
bot_response = generate_bot_reply(incoming_message)

print("--- رد الروبوت التلقائي ---")
print(bot_response)